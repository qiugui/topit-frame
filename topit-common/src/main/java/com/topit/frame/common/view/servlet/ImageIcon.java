package com.topit.frame.common.view.servlet;

public class ImageIcon {
    /**
     * 图标名字
     */
	private String name;
	/**
	 * 图标路径
	 */
    private String path;
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getPath() {
		return path;
	}
	public void setPath(String path) {
		this.path = path;
	}
}  
